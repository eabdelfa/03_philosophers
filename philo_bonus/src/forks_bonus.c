#include "philo_bonus.h"

void	take_forks(t_philo *philo)
{
	sem_wait(philo->rules->forks);
	print_state(philo, "has taken a fork");
	sem_wait(philo->rules->forks);
	print_state(philo, "has taken a fork");
}

void	release_forks(t_philo *philo)
{
	sem_post(philo->rules->forks);
	sem_post(philo->rules->forks);
}
